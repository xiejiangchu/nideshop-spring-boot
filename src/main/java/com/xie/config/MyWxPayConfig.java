package com.xie.config;

import org.springframework.beans.factory.annotation.Value;
import org.springframework.stereotype.Component;

@Component
public class MyWxPayConfig {
    //微信支付密匙
    private static String mchKey = "";
    //小程序ID
    private static String appId = "";
    //商户号
    private static String mch_id = "";
    //小程序密匙
    private static String appSecret = "";
    //通知地址
    private static String notify_url = "";

    public static String getAppKey() {
        return mchKey;
    }


    public static String getMchKey() {
        return mchKey;
    }

    @Value("${wx.pay.mchKey}")
    public void setMchKey(String mchKey) {
        MyWxPayConfig.mchKey = mchKey;
    }

    public static String getAppId() {
        return appId;
    }

    @Value("${wx.pay.appId}")
    public void setAppId(String appId) {
        MyWxPayConfig.appId = appId;
    }

    public static String getMch_id() {
        return mch_id;
    }

    @Value("${wx.pay.mch_id}")
    public void setMch_id(String mch_id) {
        MyWxPayConfig.mch_id = mch_id;
    }

    public static String getAppSecret() {
        return appSecret;
    }

    @Value("${wx.pay.appSecret}")
    public void setAppSecret(String appSecret) {
        MyWxPayConfig.appSecret = appSecret;
    }

    public static String getNotify_url() {
        return notify_url;
    }

    @Value("${wx.pay.notify_url}")
    public void setNotify_url(String notify_url) {
        MyWxPayConfig.notify_url = notify_url;
    }
}
